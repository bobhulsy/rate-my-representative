// Helper function to get test data by state/location
export const getTestOfficialsByLocation = (params) => {
  const { state, zip, lat, lng } = params;
  
  // Simple filtering based on parameters
  if (state) {
    return testOfficials.filter(official => official.state === state.toUpperCase());
  }
  
  // ZIP code to state mapping for test data
  const zipToState = {
    '27713': 'NC', // Durham, NC
    '35801': 'AL', // Huntsville, AL  
    '80301': 'CO', // Boulder, CO
    '73301': 'OK', // Oklahoma City, OK
    '48104': 'MI', // Ann Arbor, MI
    '19103': 'PA', // Philadelphia, PA
    '99501': 'AK'  // Anchorage, AK
  };
  
  if (zip && zipToState[zip]) {
    const targetState = zipToState[zip];
    return testOfficials.filter(official => official.state === targetState);
  }
  
  // Default: return all test officials
  return testOfficials;
};

